public class Data_Type {
    public static void main(String[] args) {
        byte a=127;    // -128 to 127
        System.out.println(a);
        short b=32767;    // -32768 to 32767
         System.out.println(b);
         int c=1234;         // -2147483648  to 2147483647
         System.out.println(c);
         long d=123456;        // -9223372036854775808 to 9223372036854775807
         System.out.println(d);
         float e=2365.6f;        // -3.4*10^38 to 3.4*10^38
         System.out.println(e);
         double f=456789.9376464;       
         System.out.println(f);
         char g='$';                // -128 to 127
         System.out.println(g);
         boolean h=true;    
         System.out.println(h);
    }
    
}
